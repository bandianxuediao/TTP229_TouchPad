//初始化IIC
void PAD_IIC_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_APB2PeriphClockCmd( RCC_APB2Periph_GPIOB | RCC_APB2Periph_GPIOE, ENABLE );  //使能GPIOB时钟

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_6 | GPIO_Pin_7;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP ;   //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOB, &GPIO_InitStructure);
	GPIO_SetBits(GPIOB, GPIO_Pin_6 | GPIO_Pin_7); //PB6,PB7 输出高

	GPIO_InitStructure.GPIO_Pin  = GPIO_Pin_12 | GPIO_Pin_14; //中断引脚
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPU; //设置成上拉输入
	GPIO_Init(GPIOE, &GPIO_InitStructure);

}



//产生IIC起始信号
void PAD_IIC_Start(void)
{
	PAD_SDA_OUT();     //sda线输出
	PAD_IIC_SDA = 1;
	PAD_IIC_SCL = 1;
	delay_us(4);
	PAD_IIC_SDA = 0; //START:when CLK is high,DATA change form high to low
	delay_us(4);
	PAD_IIC_SCL = 0; //钳住I2C总线，准备发送或接收数据
}
//产生IIC停止信号
void PAD_IIC_Stop(void)
{
	PAD_SDA_OUT();//sda线输出
	PAD_IIC_SCL = 0;
	PAD_IIC_SDA = 0; //STOP:when CLK is high DATA change form low to high
	delay_us(4);
	PAD_IIC_SCL = 1;
	PAD_IIC_SDA = 1; //发送I2C总线结束信号
	delay_us(4);
}
//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u8 PAD_IIC_Wait_Ack(void)
{
	u8 ucErrTime = 0;
	PAD_SDA_IN();      //SDA设置为输入
	PAD_IIC_SDA = 1;
	delay_us(1);
	PAD_IIC_SCL = 1;
	delay_us(1);

	while(PAD_READ_SDA)
	{
		ucErrTime++;

		if(ucErrTime > 250)
		{
			PAD_IIC_Stop();
			return 1;
		}
	}

	PAD_IIC_SCL = 0; //时钟输出0
	return 0;
}
//产生ACK应答
void PAD_IIC_Ack(void)
{
	PAD_IIC_SCL = 0;
	PAD_SDA_OUT();
	PAD_IIC_SDA = 0;
	delay_us(2);
	PAD_IIC_SCL = 1;
	delay_us(2);
	PAD_IIC_SCL = 0;
}
//不产生ACK应答
void PAD_IIC_NAck(void)
{
	PAD_IIC_SCL = 0;
	PAD_SDA_OUT();
	PAD_IIC_SDA = 1;
	delay_us(2);
	PAD_IIC_SCL = 1;
	delay_us(2);
	PAD_IIC_SCL = 0;
}
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答
void PAD_IIC_Send_Byte(u8 txd)
{
	u8 t;
	PAD_SDA_OUT();
	PAD_IIC_SCL = 0; //拉低时钟开始数据传输

	for(t = 0; t < 8; t++)
	{
		PAD_IIC_SDA = (txd & 0x80) >> 7;
		txd <<= 1;
		delay_us(2);   //对TEA5767这三个延时都是必须的
		PAD_IIC_SCL = 1;
		delay_us(2);
		PAD_IIC_SCL = 0;
		delay_us(2);
	}
}
